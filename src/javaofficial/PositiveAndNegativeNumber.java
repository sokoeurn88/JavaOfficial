package javaofficial;

public class PositiveAndNegativeNumber {

	public static void main(String[] args) {
		
		int num = 15;
		if(num>0) {
			System.out.println("Positive Number");
		} else if(num<0) {
			System.out.println("Negative Number");
		} else {
			System.out.println("Number is zero");
		}

	}

}
